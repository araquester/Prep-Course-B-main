var array = [2,5,6,8,10,3];
function encuentraPares(array)
{
let nuevoarray = [];
for(var i=0;i<array.length;i++)
    {
    if (array[i]%2==0)
    {
    nuevoarray.push = array[i];
    }
    }
    return(nuevoarray)
}
